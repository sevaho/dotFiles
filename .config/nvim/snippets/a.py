import sys
import getopt

# -----------------------------------------------------------------------------------------------------------------------------
# GLOBAL VARIABLES
# -----------------------------------------------------------------------------------------------------------------------------

CONSTANT_A = 100

# -----------------------------------------------------------------------------------------------------------------------------
# CLASSES
# -----------------------------------------------------------------------------------------------------------------------------


class Main(object):

    arg1 = ''

    def __init__ (self):

        print("arg: %s" % (self.arg1))

# -----------------------------------------------------------------------------------------------------------------------------
# FUNCTIONS
# -----------------------------------------------------------------------------------------------------------------------------


def usage ():

    print("Usage: %s [OPTIONS]... [ARGS]... \
            \n \
            \n  description\
            \n \
            \nOPTIONS:\
            \n \
            \n  -h, *       display the help and exit\
            \n \
            \nEXAMPLES:\
            \n \
            \nNOTE:\
            \n \
            " % (sys.argv[0]))

    sys.exit(1)

# -----------------------------------------------------------------------------------------------------------------------------
# MAIN
# -----------------------------------------------------------------------------------------------------------------------------


def main (argv):

    try:

        opts, args = getopt.getopt(argv, "ha:", ["arg1="])

    except getopt.GetoptError:

        usage()

    for opt, arg in opts:

        if opt == '-h':
            usage()
        elif opt in ("-a", "--arg1"):
            print(arg)

    # begin main code

    main = Main()


if __name__ == "__main__":

    main(sys.argv[1:])
